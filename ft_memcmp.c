#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	while (n > 0 && *(char*)s1 == *(char*)s2)
	{
		s1++;
		s2++;
		n--;
	}
	return (*(char*)s1 - *(char*)s2);
}
